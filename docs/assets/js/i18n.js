export function getLang() {
  return localStorage.getItem('aethelgard-lang') || 'de';
}

export function setLang(lang) {
  localStorage.setItem('aethelgard-lang', lang);
}

export function t(page, key) {
  const lang = getLang();
  return LANG[lang]?.[page]?.[key] ?? LANG['de']?.[page]?.[key] ?? key;
}

export function initLangToggle(onSwitch) {
  const btn = document.getElementById('lang-toggle');
  if (!btn) return;
  btn.textContent = t('nav', 'toggle');
  btn.onclick = () => {
    setLang(getLang() === 'de' ? 'en' : 'de');
    onSwitch();
  };
}
